// use of math objects

console.log(Math.PI); //value of pi

console.log(Math.max(29, 90, 30, 30)); //max num

console.log(Math.min(29, 10, 89, 90)); //min num

console.log(Math.round(6.7)); //round off
console.log(Math.floor(1.9)); //just smaller int

console.log(Math.ceil(1.1)); //just bigger int

console.log(Math.abs(-1)); //neg to pos and pos to pos

console.log(Math.random()); //num zero to  1 random

console.log(Math.sqrt(4)); //square root

console.log(Math.pow(2, 10)); //2^10

